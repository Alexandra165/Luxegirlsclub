// Функция для смены языка
window.switchLanguage = function(lang) {
    console.log("Switching language to:", lang);

    if (!window.translations[lang]) {
        console.error("Translations not found for language:", lang);
        return;
    }

    // Переводим все элементы с data-translate
    document.querySelectorAll("[data-translate]").forEach(el => {
        const key = el.getAttribute("data-translate");
        if (translations[lang][key]) {
            if (el.tagName === "INPUT" && el.type === "submit") {
                el.value = translations[lang][key];
            } else {
                el.textContent = translations[lang][key];
            }
        }
    });

    // Переводим плейсхолдеры
    document.querySelectorAll("input[data-translate-placeholder]").forEach(el => {
        const key = el.getAttribute("data-translate-placeholder");
        if (translations[lang][key]) {
            el.placeholder = translations[lang][key];
        }
    });

    // Переводим опции в select
    document.querySelectorAll("select[data-translate-options]").forEach(select => {
        const prefix = select.getAttribute("data-translate-options");
        Array.from(select.options).forEach(option => {
            const key = prefix + "_" + option.value;
            if (translations[lang][key]) {
                option.text = translations[lang][key];
            }
        });
    });

    // Сохраняем выбранный язык
        // Сохраняем выбранный язык
    localStorage.setItem("selectedLanguage", lang);

    // 💡 Обновляем юридический текст, если функция есть
    if (typeof updateLegalText === 'function') {
        updateLegalText();
    }
};


